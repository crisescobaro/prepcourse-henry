function esVerdadero(valor) {
  // Si "valor" es verdadero, retorna "Soy verdadero".
  // Caso contrario, retorna "Soy falso".
  // Tu c√≥digo:
  let i = 0;
  while(i < 1){
    if( valor === valor && valor > 0){
      return "Soy verdadero";
    }else{
      return "Soy falso";
    }
    i++
}
}

